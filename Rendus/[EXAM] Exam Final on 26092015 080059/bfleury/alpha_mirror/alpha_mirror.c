#include <unistd.h>

int		main(int ac, char **av)
{
	char	c;
	int		i;

	i = 0;
	if (ac == 2)
	{
		while (av[1][i])
		{
			if (av[1][i] >= 'a' && av[1][i] <= 'm')
			{
				c = ('n' + ('m' - av[1][i]));
				write(1, &c, 1);
			}
			else if (av[1][i] >= 'n' && av[1][i] <= 'z')
			{
				c = ('m' - (av[1][i] - 'n'));
				write(1, &c, 1);
			}
			else if (av[1][i] >= 'A' && av[1][i] <= 'M')
			{
				c = ('N' + ('M' - av[1][i]));
				write(1, &c, 1);
			}
			else if (av[1][i] >= 'N' && av[1][i] <= 'Z')
			{
				c = ('M' - (av[1][i] - 'N'));
				write(1, &c, 1);
			}
			else
				write(1, &av[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
