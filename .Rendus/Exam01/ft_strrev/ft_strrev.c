#include <stdio.h>

char	*ft_strrev(char *str)
{
	int i;
	int j;
	char tmp;

	i = 0;
	j = 0;
	while (str[i])
		i++;
	while (j < i)
	{
		tmp = str[i-j];
		str[i-j] = str[j];
		str[j] = tmp;
		j++;
	}
	return (str);
}
