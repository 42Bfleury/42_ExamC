#include <unistd.h>

int		main(int ac, char **av)
{
	char result;
	int i;
	int j;

	result = '0';
	i = 0;
	j = 0;
	if (ac == 3)
	{
		while (av[2][j])
		{
			if (av[1][i] == av[2][j])
			{
				result = '1';
				i++;
			}
			j++;
		}
		if (!av[2][j] && av[1][i])
			result = '0';
		write(1, &result, 1);
	}
	write(1, "\n", 1);
	return (0);
}
